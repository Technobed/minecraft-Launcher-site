/* Apply language */
function applyLicenseLanguage(lang) {
  const data = licenseData[lang];
  if (!data) return;

  // タブタイトル
  document.title = data.page_title;

  // ページタイトル
  document.getElementById("license_title").textContent = data.title;

  // 本文
  document.getElementById("license_text").textContent = data.text;

  // ナビゲーション
  document.getElementById("nav_download").textContent = data.nav_download;
  document.getElementById("nav_addon").textContent = data.nav_addon;
  document.getElementById("nav_license").textContent = data.nav_license;

  // PDF ボタン
  document.getElementById("license_pdf").textContent = data.pdf_button;
}

/* Language switcher */
document.getElementById("languageSelect").addEventListener("change", (e) => {
  applyLicenseLanguage(e.target.value);
});

/* Initialize */
document.addEventListener("DOMContentLoaded", () => {
  applyLicenseLanguage("ja");
});